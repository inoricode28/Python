import math

#Proceso

num1 = int(input("Ingrese el número: "))
potencia2 =  math.pow(num1, 2)
potencia3 =  math.pow(num1, 3)

#Resultado

print("El cuadrado del número ",num1,"es:", potencia2)
print("El cubo del número ",num1,"es:", potencia3)
print("El factorial del número ",num1,"es:",math.factorial(num1))