#Ingreso de datos

B = 5
b = 3
h = 10

#Proceso

Area = ((B + b)*h)/2

#Salida de datos

print ("El área del rombo es igual a:", Area)
print ("Para calcular el Área de un rombo debemos de sumar base mayor {} más base menor {} multiplicado por altura {} entre 2, lo que daria como resultado:{}".format(5,3,10,Area))